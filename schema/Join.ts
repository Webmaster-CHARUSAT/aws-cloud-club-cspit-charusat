import mongoose from "mongoose";

const JoinSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Join = mongoose.models.join || mongoose.model("join", JoinSchema);

export default Join;
