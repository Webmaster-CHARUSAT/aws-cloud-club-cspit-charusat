import mongoose from "mongoose";

const CollaborateSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    company: {
      type: String,
    },
    message: {
      type: String,
      required: true,
    },
    date: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

const Collaborate =
  mongoose.models.collaborate ||
  mongoose.model("collaborate", CollaborateSchema);

export default Collaborate;
